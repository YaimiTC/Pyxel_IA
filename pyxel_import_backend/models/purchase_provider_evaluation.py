from odoo import models, fields, api, _
from collections import defaultdict
from odoo.exceptions import ValidationError,UserError
import logging

_logger = logging.getLogger(__name__)


class PurchaseProviderEvaluation(models.Model):
    _name = 'purchase.provider.evaluation'
    _description = 'Supplier Evaluation'
    _inherit = ['mail.thread', 'mail.activity.mixin']

    name = fields.Char(string="Reference", required=True, default='/')
    sale_order_id = fields.Many2one('sale.order', string='Quotation')
    evaluation_line_ids = fields.One2many('purchase.provider.evaluation.line', 'evaluation_id',
                                          string='Evaluation Lines')

    purchase_order_ids = fields.One2many(
        'purchase.order',
        'evaluation_id',
        string='Purchase Orders',
        tracking=True
    )

    provider_names = fields.Char(string="Providers", compute="_compute_purchase_provider", store=True)

    def _compute_purchase_provider(self):
        for evaluacion in self:
            providers = self.env['purchase.order'].search([('evaluation_id', '=', evaluacion.id)])
            providers_names = providers.mapped('partner_id.name')
            evaluacion.provider_names = ', '.join(sorted(set(providers_names)))

    has_evaluations_to_apply = fields.Boolean(
        compute='_compute_has_evaluations_to_apply',
        string='Has Applicable Evaluations'
    )

    state = fields.Selection([
        ('draft', 'Draft'),
        ('evaluated', 'Evaluated'),
        ('po_created', 'Orders Generated'),
        ('cancelled', 'Cancelled'),
        ('apply', 'Applied'),
        ('evaluating_offer', 'Offer Evaluation'),
    ], default='draft', string='State', tracking=True)

    purchase_order_count = fields.Integer(string="Number of Orders", compute='_compute_purchase_order_count',
                                          store=True)

    cost_line_temp_ids = fields.One2many('purchase.provider.evaluation.cost.line', 'evaluation_id',
                                         string='Additional Costs')

    final_sale_order_id = fields.Many2one('sale.order', string='Final Generated Offer', readonly=True)

    @api.depends('purchase_order_ids')
    def _compute_purchase_order_count(self):
        for record in self:
            record.purchase_order_count = len(record.purchase_order_ids)

    def action_generate_purchase_orders(self):
        """Generates purchase orders by supplier based on the evaluation."""
        PurchaseOrder = self.env['purchase.order']
        grouped_lines = defaultdict(list)

        # Group by supplier
        for line in self.evaluation_line_ids:
            if not line.suggested_supplier_id:
                raise ValidationError(f'No supplier defined for the product {line.product_id.display_name}')
            grouped_lines[line.suggested_supplier_id].append(line)

        created_pos = []

        for supplier, lines in grouped_lines.items():
            po = PurchaseOrder.create({
                'partner_id': supplier.id,
                'sale_order_id': self.sale_order_id.id,
                'evaluation_id': self.id,
                'order_line': [(0, 0, {
                    'product_id': line.product_id.id,
                    'product_qty': line.product_uom_qty,
                    'product_uom': line.product_uom.id,
                    'price_unit': line.price_unit or 0.0,
                    'date_planned': fields.Date.today(),
                    'sale_order_line_id': line.sale_order_line_id.id,
                    'name': line.product_id.name,
                }) for line in lines],
            })
            created_pos.append(po)
            self.state = 'po_created'
        return {
            'type': 'ir.actions.act_window',
            'name': 'Generated Purchase Orders',
            'res_model': 'purchase.order',
            'view_mode': 'tree,form',
            'domain': [('id', 'in', [po.id for po in created_pos])],
        }

    @api.depends('purchase_order_ids.state')
    def _compute_has_evaluations_to_apply(self):
        for evaluation in self:
            # If any order is in a state other than 'draft' or 'cancel', it can be applied
            if evaluation.purchase_order_ids:
                evaluation.has_evaluations_to_apply = any(
                    po.state not in ('draft', 'cancel') for po in evaluation.purchase_order_ids
                )
            else:
                evaluation.has_evaluations_to_apply = False

    def _check_apply_status(self):
        for evaluation in self:
            if evaluation.state != 'apply':
                # If any of the orders generated by this evaluation is in 'done' state
                related_orders = self.env['purchase.order'].search([
                    ('evaluation_id', '=', evaluation.id),
                    ('state', '=', 'done')
                ])
                if related_orders:
                    evaluation.state = 'apply'

    @api.depends('purchase_order_ids')
    def _compute_state(self):
        for rec in self:
            if not rec.purchase_order_ids:
                rec.state = 'draft'

    def unlink(self):
        for record in self:
            # Only allowed if in 'draft' or 'cancelled' state
            if record.state not in ['draft', 'cancelled']:
                raise ValidationError(_("Evaluations can only be deleted in Draft or Cancelled state."))

            # Check if all orders are cancelled or there are no orders
            if record.purchase_order_ids:
                if any(p.state != 'cancel' for p in record.purchase_order_ids):
                    raise ValidationError(_("All orders must be cancelled to delete this evaluation."))

        return super(PurchaseProviderEvaluation, self).unlink()

    def action_revaluate(self):
        for record in self:
            if record.state != 'draft':
                raise ValidationError(_("You can only re-evaluate evaluations in draft state."))
            if record.purchase_order_ids:
                raise ValidationError(_("This evaluation already has associated orders."))

            # Your logic for regenerating orders would go here
            record.action_generate_purchase_orders()

    @api.model
    def create(self, vals):
        sale_order_id = vals.get('sale_order_id')
        if sale_order_id:
            existing = self.search([
                ('sale_order_id', '=', sale_order_id),
                ('state', '=', 'apply'),
            ], limit=1)
            if existing:
                raise ValidationError("An applied evaluation already exists for this sales order. Cannot create another.")
        sale_order = self.env['sale.order'].browse(sale_order_id)
        if sale_order and not sale_order.process_id:
            process = self.env['sale.order.process'].create({'state': 'open'})
            sale_order.process_id = process.id

        if vals.get('name', '/') == '/':
            sale_order = self.env['sale.order'].browse(vals.get('sale_order_id'))
            if not sale_order:
                raise ValidationError("A quotation (sale.order) is required to generate the sequence.")

            # Change the sale.order type to initial_evaluation
            sale_order.order_type = 'evaluation_initial'
            sale_order_ref = sale_order.name
            # Count how many evaluations already exist for this sale_order
            count = self.search_count([('sale_order_id', '=', sale_order.id)]) + 1
            vals['name'] = f"EVAL/{sale_order_ref}/{count:02d}"
        return super().create(vals)

    def copy(self, default=None):
        self.ensure_one()
        if self.sale_order_id:
            existing = self.search([
                ('sale_order_id', '=', self.sale_order_id.id),
                ('state', '=', 'apply'),
                ('id', '!=', self.id),
            ], limit=1)
            if existing:
                raise ValidationError("An applied evaluation already exists for this sales order. Cannot duplicate.")
        return super().copy(default)

    def action_generate_eval_offer(self):
        self.ensure_one()
        if self.final_sale_order_id:
            raise ValidationError("The Final Offer has already been generated for this process.")

        # Customer from the original quotation
        partner = self.sale_order_id.partner_id

        # Create new sales order
        final_so = self.env['sale.order'].create({
            'partner_id': partner.id,
            'order_type': 'evaluation_final',
            'evaluation_apply_id': self.id,
            'origin': self.name,
            'opportunity_id': self.sale_order_id.opportunity_id.id,
            'order_line': []
        })

        order_lines = []

        # Add lines with stock from the original quotation
        for line in self.sale_order_id.order_line:
            if line.product_id.qty_available >= line.product_uom_qty:
                order_lines.append((0, 0, {
                    'product_id': line.product_id.id,
                    'product_uom_qty': line.product_uom_qty,
                    'product_uom': line.product_uom.id,
                    'price_unit': line.price_unit,
                    'name': line.name,
                }))

        for po in self.purchase_order_ids:
            for po_line in po.order_line:
                order_lines.append((0, 0, {
                    'product_id': po_line.product_id.id,
                    'product_uom_qty': po_line.product_qty,
                    'product_uom': po_line.product_uom.id,
                    'price_unit': po_line.price_unit,
                    'name': f"Imported from PO {po.name}",
                }))

        # Add consolidated additional costs
        for cost_line in self.cost_line_temp_ids:
            # Add the actual cost value if it is a percentage, distributing it
            amount = cost_line.amount
            if cost_line.distribution_type == 'percentage':
                amount = sum((po.amount_total * (cost_line.amount / 100)) for po in self.purchase_order_ids)
            order_lines.append((0, 0, {
                'product_id': cost_line.product_id.id,
                'name': f"Additional cost: {cost_line.name}",
                'product_uom_qty': 1,
                'product_uom': cost_line.product_id.uom_id.id,
                'price_unit': amount,
            }))

        # Write the lines
        # providers_names = self.purchase_order_ids.mapped('partner_id.name')
        # , 'provider_names': ', '.join(sorted(set(providers_names)))

        final_so.write({'order_line': order_lines})
        self.final_sale_order_id = final_so.id
        self.state = "evaluating_offer"

        return {
            'type': 'ir.actions.act_window',
            'res_model': 'sale.order',
            'view_mode': 'form',
            'res_id': final_so.id,
            'target': 'current',
        }

    def action_send_all_rfq_emails(self):
        self.ensure_one()

        template = self.env.ref('purchase.email_template_edi_purchase', raise_if_not_found=False)
        if not template:
            raise UserError(_("No se encontró la plantilla de correo para órdenes de compra."))

        pos_to_send = self.purchase_order_ids.filtered(lambda po: po.state in ('draft', 'sent'))
        if not pos_to_send:
            raise UserError(_("No hay órdenes de compra en estado borrador o enviadas para enviar."))

        total_sent = 0
        for po in pos_to_send:
            try:
                template.send_mail(po.id, force_send=True)
                po.write({'state': 'sent'})  # Marcar como enviada
                total_sent += 1
            except Exception as e:
                po.message_post(body=_("Error al enviar correo: %s") % str(e))

        # Publicar mensaje en el chatter de la evaluación
        self.message_post(
            body=_("Se enviaron %s RFQs a proveedores desde la evaluación.") % total_sent,
            message_type="comment",
            subtype_xmlid="mail.mt_note"
        )

        return {
            'type': 'ir.actions.client',
            'tag': 'display_notification',
            'params': {
                'title': _('Éxito'),
                'message': _('Se enviaron correctamente %s RFQs.') % total_sent,
                'type': 'success',
                'sticky': False,
            }
        }


class PurchaseProviderEvaluationLine(models.Model):
    _name = 'purchase.provider.evaluation.line'
    _description = 'Supplier Evaluation by Product'

    evaluation_id = fields.Many2one('purchase.provider.evaluation', string='Evaluation')
    sale_order_line_id = fields.Many2one('sale.order.line', string='Quotation Line')
    product_id = fields.Many2one('product.product', string='Product', required=True)
    product_uom_qty = fields.Float(string='Quantity', required=True)
    product_uom = fields.Many2one('uom.uom', string='Unit of Measure', required=True)
    price_unit = fields.Float(string='Estimated Price')  # This will be used in the purchase order

    suggested_supplier_id = fields.Many2one('res.partner', string='Suggested Supplier')
    supplier_ids = fields.Many2many('res.partner', string='Available Suppliers', compute='_compute_suppliers',
                                    store=False)

    @api.depends('product_id')
    def _compute_suppliers(self):
        for rec in self:
            rec.supplier_ids = rec.product_id.seller_ids.mapped('partner_id')


class EvaluationCostLine(models.Model):
    _name = 'purchase.provider.evaluation.cost.line'
    _description = 'Temporary Additional Cost'

    evaluation_id = fields.Many2one('purchase.provider.evaluation', ondelete='cascade')
    product_id = fields.Many2one(
        'product.product',
        string='Service',
        domain=[('detailed_type', '=', 'service')],
        required=True
    )
    name = fields.Char(compute='_compute_name', required=True)
    amount = fields.Float(required=True)
    distribution_type = fields.Selection([
        ('fixed', 'Fixed Amount'),
        ('percentage', 'Percentage'),
    ], required=True)
    is_cost_special = fields.Boolean()
    purchase_ids = fields.Many2many('purchase.order', string='Purchase Orders Applied',
                                    domain="[('id', 'in', parent.purchase_order_ids)]")

    @api.depends('product_id')
    def _compute_name(self):
        for record in self:
            record.name = record.product_id.display_name or ''